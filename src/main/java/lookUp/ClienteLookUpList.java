package lookUp;

public class ClienteLookUpList {
	private int n_cliente;
	private String cpf;
	private String nome;
	private String telefone;
	private int n_alugueis;
	public int getN_cliente() {
		return n_cliente;
	}
	public void setN_cliente(int n_cliente) {
		this.n_cliente = n_cliente;
	}
	public String getCpf() {
		return cpf;
	}
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getTelefone() {
		return telefone;
	}
	public void setTelefone(String telefone) {
		this.telefone = telefone;
	}
	public int getN_alugueis() {
		return n_alugueis;
	}
	public void setN_alugueis(int n_alugueis) {
		this.n_alugueis = n_alugueis;
	}
	
	
}
